// Helper to get summary stats
export function getOnboardingStats(apps: OnboardingApplication[]) {
  const active = apps.filter(a => a.status !== "Rejected" && a.status !== "Approved");
  return {
    total: apps.length,
    drafts: apps.filter(a => a.status === "Draft").length,
    inProgress: apps.filter(a => a.status === "In Progress").length,
    pendingApproval: apps.filter(a => a.status === "Pending Approval").length,
    rejected: apps.filter(a => a.status === "Rejected").length,
    approved: apps.filter(a => a.status === "Approved").length,
    avgPolicyCompletion: active.length > 0
      ? Math.round(active.reduce((s, a) => s + a.policyCompletion.percentComplete, 0) / active.length)
      : 0,
    referrals: apps.filter(a => a.manualReviewItems.length > 0).length,
  };
}
